namespace con4gis\FrameworkBundle\Classes\Tables;

class TableButton implements \JsonSerializable
{
    private $label = '';

    private $href = '';

    /**
     * @return string
     */
    public function getLabel(): string
    {
        return $this->label;
    }

    /**
     * @param string $label
     */
    public function setLabel(string $label): void
    {
        $this->label = $label;
    }

    /**
     * @return string
     */
    public function getHref(): string
    {
        return $this->href;
    }

    /**
     * @param string $href
     */
    public function setHref(string $href): void
    {
        $this->href = $href;
    }

    public function jsonSerialize()
    {
        return [
            'label' => $this->label,
            'href' => $this->href,
        ];
    }
}
